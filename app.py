import streamlit as st
import os
import base64
import random
import time
import streamlit.components.v1 as components
from streamlit_js_eval import streamlit_js_eval 
# C·∫ßn c√†i ƒë·∫∑t: pip install streamlit-js-eval

# ================== C·∫§U H√åNH & TR·∫†NG TH√ÅI ==================
st.set_page_config(page_title="T·ªï B·∫£o D∆∞·ª°ng S·ªë 1", layout="wide")

VIDEO_PC = "airplane.mp4"
VIDEO_MOBILE = "mobile.mp4"
BG_PC = "cabbase.jpg"
BG_MOBILE = "mobile.jpg"
MUSIC_FILES = ["background.mp3", "background2.mp3", "background3.mp3", "background4.mp3", "background5.mp3"]

if "intro_done" not in st.session_state:
    st.session_state.intro_done = False
if "is_mobile" not in st.session_state:
    st.session_state.is_mobile = None

# --- X√ÅC ƒê·ªäNH THI·∫æT B·ªä NGAY L·∫¨P T·ª®C ---
if st.session_state.is_mobile is None:
    js_result = streamlit_js_eval(js_expressions='(/Mobi|Android|iPhone|iPad/i.test(navigator.userAgent))', 
                                 key=f'mobile_detector_{time.time()}')
    
    if js_result is not None:
        st.session_state.is_mobile = js_result
        st.rerun()
    else:
        # Gi·∫£ ƒë·ªãnh PC trong khi ch·ªù
        st.session_state.is_mobile = False

# ================== ·∫®N HEADER STREAMLIT ==================
def hide_streamlit_ui():
    st.markdown("""
    <style>
    [data-testid="stToolbar"],
    header, footer, iframe[title*="keyboard"], [tabindex="0"][aria-live] {
        display: none !important;
        visibility: hidden !important;
    }
    </style>
    """, unsafe_allow_html=True)


# ================== M√ÄN H√åNH INTRO ƒê√É S·ª¨A L·ªñI CHIA ƒê√îI ==================
def intro_screen(is_mobile=False):
    hide_streamlit_ui()
    
    video_path = VIDEO_MOBILE if is_mobile else VIDEO_PC
    
    if not os.path.exists(video_path):
        st.error(f"‚ö†Ô∏è Kh√¥ng t√¨m th·∫•y video: {video_path}")
        st.session_state.intro_done = True
        st.rerun()
        return

    # M√£ h√≥a video th√†nh base64
    with open(video_path, "rb") as f:
        video_b64 = base64.b64encode(f.read()).decode()

    # THAY ƒê·ªîI QUAN TR·ªåNG: S·ª≠ d·ª•ng Vh v√† Vw tri·ªát ƒë·ªÉ, lo·∫°i b·ªè margin/padding
    intro_html = f"""
    <!DOCTYPE html>
    <html lang="vi">
    <head>
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <style>
        html, body {{ 
            margin: 0 !important; 
            padding: 0 !important; 
            height: 100vh; 
            width: 100vw; 
            overflow: hidden; 
            background-color: black; 
        }}
        /* ƒê·∫£m b·∫£o iframe con chi·∫øm to√†n b·ªô kh√¥ng gian */
        iframe, #intro-container {{ 
            position: absolute; 
            top: 0; 
            left: 0; 
            width: 100vw; 
            height: 100vh; 
        }}
        video {{ 
            width: 100vw; 
            height: 100vh; 
            object-fit: cover; 
            object-position: center; 
        }}
        #intro-text {{
            position: fixed; bottom: 18%; left: 50%; transform: translateX(-50%);
            font-size: clamp(1em, 4vw, 2em); color: white; z-index: 10;
            font-family: 'Playfair Display', serif; text-shadow: 2px 2px 6px rgba(0,0,0,0.8);
            animation: fadeInOut 6s ease-in-out forwards;
        }}
        @keyframes fadeInOut {{
            0% {{ opacity: 0; transform: translate(-50%, 20px); }} 20% {{ opacity: 1; transform: translate(-50%, 0); }}
            80% {{ opacity: 1; transform: translate(-50%, 0); }} 100% {{ opacity: 0; transform: translate(-50%, -10px); }}
        }}
        #fade {{
            position: fixed; top: 0; left: 0; width: 100vw; height: 100vh;
            background: black; opacity: 0; z-index: 10;
            transition: opacity 1s ease-in-out;
        }}
    </style>
    </head>
    <body>
        <video id="introVid" autoplay muted playsinline>
            <source src="data:video/mp4;base64,{video_b64}" type="video/mp4">
        </video>
        <div id="intro-text">KH√ÅM PH√Å TH·∫æ GI·ªöI C√ôNG CH√öNG T√îI</div>
        <div id="fade"></div>
        <script>
            const vid = document.getElementById("introVid");
            const fade = document.getElementById("fade");

            // Sau khi video k·∫øt th√∫c, g·ª≠i t√≠n hi·ªáu ho√†n th√†nh
            vid.onended = () => {{
                fade.style.opacity = 1;
                setTimeout(() => {{
                    // G·ª≠i t√≠n hi·ªáu ho√†n th√†nh intro
                    window.parent.postMessage({{"type": "intro_done"}}, "*");
                }}, 1200);
            }};
            
            // X·ª≠ l√Ω l·ªói ph√°t ho·∫∑c kh√¥ng autoplay ƒë∆∞·ª£c
            vid.onerror = vid.onpause = () => {{
                 // N·∫øu video d·ª´ng do l·ªói ho·∫∑c ng∆∞·ªùi d√πng can thi·ªáp, ta v·∫´n chuy·ªÉn trang sau 9s
                 setTimeout(() => {{
                     window.parent.postMessage({{"type": "intro_done"}}, "*");
                 }}, 9000); // 9 gi√¢y gi·∫£ ƒë·ªãnh
            }};

            // ƒê·∫£m b·∫£o video lu√¥n b·∫Øt ƒë·∫ßu ph√°t
            vid.play().catch(error => {{
                console.log("Autoplay blocked, switching after delay.");
                setTimeout(() => {{
                    window.parent.postMessage({{"type": "intro_done"}}, "*");
                }}, 9000);
            }});
        </script>
    </body>
    </html>
    """
    
    # S·ª¨A L·ªñI: components.html(height=None) ho·∫∑c height=1000 ƒë·ªÉ bao tr√πm
    # height=800 v·∫´n c√≥ th·ªÉ b·ªã chia, ta d√πng gi√° tr·ªã l·ªõn h∆°n ho·∫∑c None.
    components.html(intro_html, height=1000, scrolling=False) 

    # D√πng bi·∫øn session state ƒë·ªÉ ki·ªÉm so√°t th·ªùi gian ch·∫°y.
    if "start_time" not in st.session_state:
        st.session_state.start_time = time.time()
    
    # Ki·ªÉm tra th·ªùi gian ƒë√£ tr√¥i qua
    if time.time() - st.session_state.start_time < 9.5: # 9.5 gi√¢y cho an to√†n
        time.sleep(1) # Gi·∫£m sleep time ƒë·ªÉ tr√°nh l·ªói timeout
        st.rerun()
    else:
        # Ho√†n th√†nh sau khi ƒë√£ rerun ƒë·ªß l·∫ßn
        st.session_state.intro_done = True
        del st.session_state.start_time
        st.rerun()


