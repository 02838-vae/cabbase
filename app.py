# Gi·∫£ ƒë·ªãnh th·ªùi l∆∞·ª£ng video l√† 9 gi√¢y (H√£y ƒëi·ªÅu ch·ªânh n·∫øu video c·ªßa b·∫°n kh√°c)
VIDEO_DURATION_SECONDS = 9 
