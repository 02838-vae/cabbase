# ================== M√ÄN H√åNH INTRO ==================
def intro_screen(is_mobile=False):
    hide_streamlit_ui()

    video_path = VIDEO_MOBILE if is_mobile else VIDEO_PC
    if not os.path.exists(video_path):
        st.error(f"‚ö†Ô∏è Kh√¥ng t√¨m th·∫•y video: {video_path}")
        st.session_state.intro_done = True
        st.rerun()
        return

    # M√£ h√≥a video sang Base64 ƒë·ªÉ nh√∫ng v√†o HTML
    try:
        with open(video_path, "rb") as f:
            video_b64 = base64.b64encode(f.read()).decode()
    except Exception as e:
        st.error(f"L·ªói khi ƒë·ªçc file video: {e}")
        st.session_state.intro_done = True
        st.rerun()
        return

    # --- C·∫•u h√¨nh CSS t√πy theo thi·∫øt b·ªã ---
    if is_mobile:
        object_position = "center 15%"
        text_bottom = "12%"               
        font_size = "clamp(16px, 4.5vw, 26px)"
        text_width = "92vw"
    else:
        object_position = "center center"
        text_bottom = "20%"
        font_size = "clamp(26px, 3vw, 46px)"
        text_width = "70vw"

    # ƒêi·ªÅu ch·ªânh th·ªùi gian animation ƒë·ªÉ text bi·∫øn m·∫•t tr∆∞·ªõc khi video k·∫øt th√∫c (9s)
    # 8.5s ƒë·∫£m b·∫£o text m·ªù v√† bi·∫øn m·∫•t ho√†n to√†n
    animation_duration = f"{VIDEO_DURATION_SECONDS - 0.5}s" # 8.5s

    intro_html = f"""
    <html lang="vi">
    <head>
        <meta name="viewport" content="width=device-width, initial-scale=1.0, user-scalable=no">
        <style>
            @import url('https://fonts.googleapis.com/css2?family=Cinzel:wght@700&display=swap');

            html, body {{
                margin: 0; padding: 0;
                width: 100vw;
                height: 100vh;
                overflow: hidden;
                background: black;
            }}

            video {{
                position: absolute;
                top: 0; left: 0;
                width: 100vw;
                height: 100vh;
                object-fit: cover;
                object-position: {object_position};
                z-index: 1;
            }}

            #intro-text {{
                position: absolute;
                left: 50%;
                bottom: {text_bottom};
                transform: translateX(-50%);
                font-family: 'Cinzel', serif;
                font-size: {font_size};
                width: {text_width};
                text-align: center;
                color: #fff8dc;
                font-weight: 700;
                letter-spacing: 2px;
                text-shadow: 0 0 6px rgba(255, 255, 255, 0.3), 0 0 18px rgba(255, 240, 180, 0.5);
                z-index: 10;
                opacity: 0;
                /* S·ª≠ d·ª•ng th·ªùi gian ƒë√£ ƒëi·ªÅu ch·ªânh ƒë·ªÉ text bi·∫øn m·∫•t s·ªõm h∆°n */
                animation: fadeAppear {animation_duration} ease-in-out forwards; 
                white-space: normal;
                overflow-wrap: break-word;
            }}

            /* ‚ú® cinematic fade xu·∫•t hi·ªán ‚Äì s√°ng ‚Äì tan bi·∫øn */
            @keyframes fadeAppear {{
                0% {{
                    opacity: 0;
                    transform: translate(-50%, 40px);
                    text-shadow: none;
                }}
                25% {{
                    opacity: 1;
                    text-shadow: 0 0 10px rgba(255, 240, 180, 0.8), 0 0 20px rgba(255,255,255,0.4);
                    transform: translate(-50%, 0);
                }}
                70% {{
                    opacity: 1;
                    text-shadow: 0 0 16px rgba(255, 255, 220, 0.8);
                }}
                100% {{
                    opacity: 0; /* ƒê·∫£m b·∫£o text bi·∫øn m·∫•t */
                    transform: translate(-50%, -30px);
                    text-shadow: 0 0 2px rgba(255,255,255,0.1);
                }}
            }}

            #fade {{
                position: absolute;
                top: 0; left: 0;
                width: 100%;
                height: 100%;
                background: black;
                opacity: 0;
                z-index: 9;
                transition: opacity 1.2s ease-in-out;
            }}
        </style>
    </head>
    <body>
        <video id="introVid" autoplay muted playsinline>
            <source src="data:video/mp4;base64,{video_b64}" type="video/mp4">
        </video>
        <div id="intro-text">KH√ÅM PH√Å TH·∫æ GI·ªöI C√ôNG CH√öNG T√îI</div>
        <div id="fade"></div>

        <script>
            const vid = document.getElementById("introVid");
            const fade = document.getElementById("fade");

            function finishIntro() {{
                fade.style.opacity = 1; // B·∫Øt ƒë·∫ßu chuy·ªÉn sang m√†n h√¨nh ƒëen
                setTimeout(() => {{
                    // Th√¥ng b√°o cho Streamlit ƒë·ªÉ chuy·ªÉn trang
                    window.parent.postMessage({{"type": "intro_done"}}, "*"); 
                }}, 1200); // Ch·ªù 1.2s ƒë·ªÉ m√†n h√¨nh ƒëen ho√†n to√†n
            }}

            vid.onended = finishIntro;
            vid.play().catch(() => {{
                console.log("Autoplay b·ªã ch·∫∑n, fallback {VIDEO_DURATION_SECONDS}s");
                // C∆° ch·∫ø d·ª± ph√≤ng d·ª±a tr√™n th·ªùi gian (v√≠ d·ª• 9000ms)
                setTimeout(finishIntro, {VIDEO_DURATION_SECONDS * 1000}); 
            }});
        </script>
    </body>
    </html>
    """

    # Nh√∫ng HTML component
    components.html(intro_html, height=950, scrolling=False)

    # C∆° ch·∫ø d·ª± ph√≤ng Python (n·∫øu JS th·∫•t b·∫°i)
    if st.session_state.start_time is None:
        st.session_state.start_time = time.time()
    
    # Ch·ªù l√¢u h∆°n th·ªùi l∆∞·ª£ng video m·ªôt ch√∫t (v√≠ d·ª• 9.5s)
    if time.time() - st.session_state.start_time < (VIDEO_DURATION_SECONDS + 0.5): 
        time.sleep(1)
        st.rerun()
    else:
        st.session_state.intro_done = True
        st.session_state.start_time = None
        st.rerun()

# ================== TRANG CH√çNH ==================
def main_page(is_mobile=False):
    hide_streamlit_ui()
    bg = BG_MOBILE if is_mobile else BG_PC

    try:
        with open(bg, "rb") as f:
            bg_b64 = base64.b64encode(f.read()).decode()
    except FileNotFoundError:
        st.error(f"‚ö†Ô∏è Kh√¥ng t√¨m th·∫•y ·∫£nh n·ªÅn: {bg}")
        bg_b64 = ""

    st.markdown(f"""
    <style>
    @import url('https://fonts.googleapis.com/css2?family=Playfair+Display:wght@600&display=swap');
    .stApp {{
        background-image: url("data:image/jpeg;base64,{bg_b64}");
        background-size: cover;
        background-position: center;
        background-attachment: fixed;
        animation: fadeInBg 1s ease-in-out forwards;
    }}
    @keyframes fadeInBg {{ from {{ opacity: 0; }} to {{ opacity: 1; }} }}
    h1 {{
        text-align: center; margin-top: 60px; color: #2E1C14;
        text-shadow: 2px 2px 6px #FFF8DC;
        font-family: 'Playfair Display', serif;
    }}
    </style>
    """, unsafe_allow_html=True)

    available_music = [m for m in MUSIC_FILES if os.path.exists(m)]
    if available_music:
        chosen = random.choice(available_music)
        # S·ª≠ d·ª•ng st.container() ƒë·ªÉ tr√°nh l·ªói khi st.sidebar b·ªã ·∫©n
        with st.container():
            st.subheader("üéµ Nh·∫°c n·ªÅn")
            st.audio(chosen, start_time=0)
            st.caption(f"ƒêang ph√°t: **{os.path.basename(chosen)}**")

    st.markdown("<h1>T·ªî B·∫¢O D∆Ø·ª†NG S·ªê 1</h1>", unsafe_allow_html=True)


