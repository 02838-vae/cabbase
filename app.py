# ================== M√ÄN H√åNH INTRO (FULL SCREEN & AUTOPLAY) ==================
def intro_screen(is_mobile=False):
    hide_streamlit_ui()
    
    video_path = VIDEO_MOBILE if is_mobile else VIDEO_PC
    
    if not os.path.exists(video_path):
        st.error(f"‚ö†Ô∏è Kh√¥ng t√¨m th·∫•y video: {video_path}")
        st.session_state.intro_done = True
        st.rerun()
        return

    with open(video_path, "rb") as f:
        video_b64 = base64.b64encode(f.read()).decode()

    intro_html = f"""
    <!DOCTYPE html>
    <html lang="vi">
    <head>
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <style>
        /* QUAN TR·ªåNG: CSS b√™n trong components.html */
        html, body {{ 
            margin: 0 !important; padding: 0 !important; 
            height: 100vh; width: 100vw; 
            overflow: hidden; background-color: black; 
        }}
        video {{ 
            position: absolute; /* ƒê·∫£m b·∫£o video n·∫±m trong iframe */
            top: 0; left: 0;
            width: 100vw; height: 100vh; 
            object-fit: cover; object-position: center; 
        }}
        #intro-text {{
            position: fixed; bottom: 18%; left: 50%; transform: translateX(-50%);
            font-size: clamp(1em, 4vw, 2em); color: white; z-index: 10;
            font-family: 'Playfair Display', serif; text-shadow: 2px 2px 6px rgba(0,0,0,0.8);
            animation: fadeInOut 6s ease-in-out forwards;
        }}
        @keyframes fadeInOut {{
            0% {{ opacity: 0; transform: translate(-50%, 20px); }} 20% {{ opacity: 1; transform: translate(-50%, 0); }}
            80% {{ opacity: 1; transform: translate(-50%, 0); }} 100% {{ opacity: 0; transform: translate(-50%, -10px); }}
        }}
        #fade {{
            position: fixed; top: 0; left: 0; width: 100vw; height: 100vh;
            background: black; opacity: 0; z-index: 10;
            transition: opacity 1s ease-in-out;
        }}
    </style>
    </head>
    <body>
        <video id="introVid" autoplay muted playsinline>
            <source src="data:video/mp4;base64,{video_b64}" type="video/mp4">
        </video>
        <div id="intro-text">KH√ÅM PH√Å TH·∫æ GI·ªöI C√ôNG CH√öNG T√îI</div>
        <div id="fade"></div>
        <script>
            const vid = document.getElementById("introVid");
            const fade = document.getElementById("fade");
            
            const finishIntro = () => {{
                fade.style.opacity = 1;
                setTimeout(() => {{
                    window.parent.postMessage({{"type": "intro_done"}}, "*");
                }}, 1200);
            }};

            vid.onended = finishIntro;
            
            // X·ª≠ l√Ω ch·∫∑n Autoplay: N·∫øu b·ªã ch·∫∑n, ch·ªù 9 gi√¢y r·ªìi chuy·ªÉn trang
            vid.play().catch(error => {{
                console.log("Autoplay blocked or failed, waiting 9 seconds.");
                setTimeout(finishIntro, 9000); 
            }});
        </script>
    </body>
    </html>
    """
    
    # B·ªè tham s·ªë height ƒë·ªÉ CSS fixed quy·∫øt ƒë·ªãnh
    components.html(intro_html, scrolling=False) 

    # --- C∆° ch·∫ø Chuy·ªÉn Trang d·ª±a tr√™n th·ªùi gian ---
    if "start_time" not in st.session_state:
        st.session_state.start_time = time.time()
    
    if time.time() - st.session_state.start_time < 9.5: 
        time.sleep(1) 
        st.rerun()
    else:
        st.session_state.intro_done = True
        del st.session_state.start_time
        st.rerun()


# ================== TRANG CH√çNH ==================
def main_page(is_mobile=False):
    hide_streamlit_ui() 
    bg = BG_MOBILE if is_mobile else BG_PC
    
    try:
        with open(bg, "rb") as f:
            bg_b64 = base64.b64encode(f.read()).decode()
    except FileNotFoundError:
        st.error(f"‚ö†Ô∏è Kh√¥ng t√¨m th·∫•y ·∫£nh n·ªÅn: {bg}")
        bg_b64 = ""

    st.markdown(f"""
    <style>
    @import url('https://fonts.googleapis.com/css2?family=Playfair+Display:wght@600&display=swap');
    .stApp {{
        background-image: url("data:image/jpeg;base64,{bg_b64}");
        background-size: cover;
        background-position: center;
        background-attachment: fixed;
        animation: fadeInBg 1s ease-in-out forwards;
    }}
    @keyframes fadeInBg {{ from {{ opacity: 0; }} to {{ opacity: 1; }} }}
    h1 {{
        text-align: center; margin-top: 60px; color: #2E1C14;
        text-shadow: 2px 2px 6px #FFF8DC; font-family: 'Playfair Display', serif;
    }}
    </style>
    """, unsafe_allow_html=True)

    available_music = [m for m in MUSIC_FILES if os.path.exists(m)]
    if available_music:
        chosen = random.choice(available_music)
        with st.sidebar:
            st.subheader("üéµ Nh·∫°c n·ªÅn")
            st.audio(chosen, start_time=0) 
            st.caption(f"ƒêang ph√°t: **{os.path.basename(chosen)}**")

    st.markdown("<h1>T·ªî B·∫¢O D∆Ø·ª†NG S·ªê 1</h1>", unsafe_allow_html=True)


# ================== LU·ªíNG CH√çNH ==================
hide_streamlit_ui()

if st.session_state.is_mobile is None:
    pass 
