# ================== C·∫§U H√åNH ==================
st.set_page_config(page_title="T·ªï B·∫£o D∆∞·ª°ng S·ªë 1", layout="wide")

# GI·∫¢ ƒê·ªäNH: VIDEO ƒê∆Ø·ª¢C ƒê·∫∂T TRONG TH∆Ø M·ª§C media/
VIDEO_PC = "media/airplane.mp4"
